export function getModelById(id: string): LLMModel | undefined {
  return llmModels.find((m) => m.id === id);
}

export function getModelsByProvider(provider: string): LLMModel[] {
  return llmModels.filter((m) => m.provider === provider);
}

export function getModelsByCategory(category: string): LLMModel[] {
  return llmModels.filter((m) => m.categories.includes(category as any));
}
